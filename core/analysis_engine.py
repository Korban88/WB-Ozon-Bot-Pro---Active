"""
Модуль анализа карточки товара — Card Audit Engine v2.

Принимает данные карточки и возвращает структурированный аудит:
  - Оценка CTR-факторов (главное фото, заголовок, цена/рейтинг)
  - Оценка конверсионных факторов (описание, инфографика, УТП)
  - Список конкретных проблем, мешающих продажам
  - Приоритетный action plan (quick wins → стратегические улучшения)
  - Переработанный заголовок как пример

Правило: анализ строится ТОЛЬКО на переданных данных.
Никаких выдуманных метрик конкурентов, средних по категории или
несуществующих свойств товара.
"""

from services import openrouter as llm

_SYSTEM = """Ты — эксперт по росту продаж на маркетплейсах Wildberries и Ozon.
Специализация: аудит карточек, рост CTR, оптимизация конверсии.
Даёшь конкретные, честные рекомендации. Пишешь структурированно.
Не придумываешь данные о конкурентах или средние по категории — если данных нет, так и говоришь."""


def _prompt(brief: str, marketplace: str) -> str:
    mp = {"wb": "Wildberries", "ozon": "Ozon"}.get(marketplace, marketplace)
    return f"""Проведи аудит карточки товара на {mp}.

ДАННЫЕ КАРТОЧКИ:
{brief}

Структура аудита:

━━━ 🎯 CTR-ФАКТОРЫ (влияют на клики с поисковой выдачи) ━━━
— Главное фото: X/10 + комментарий
— Заголовок/название: X/10 + что конкретно не так
— Цена и рейтинг: X/10 + комментарий

━━━ 💰 КОНВЕРСИОННЫЕ ФАКТОРЫ (влияют на покупку) ━━━
— Описание и ключевые слова: X/10
— УТП и позиционирование: X/10
— Инфографика (если данные есть): X/10

━━━ 🚨 КРИТИЧЕСКИЕ ПРОБЛЕМЫ ━━━
(что прямо сейчас мешает продажам — максимум 5 пунктов, по приоритету)

━━━ ⚡ QUICK WINS (сделать за 1-2 дня) ━━━
(конкретные действия с прогнозируемым эффектом на CTR/конверсию)

━━━ 📈 СТРАТЕГИЧЕСКИЕ УЛУЧШЕНИЯ ━━━
(что улучшить за 1-2 недели)

━━━ ✍️ ПРИМЕР ПЕРЕРАБОТАННОГО ЗАГОЛОВКА ━━━
Было: [текущий заголовок если есть]
Стало: [улучшенный вариант с SEO-ключами]

Важно: опирайся только на предоставленные данные. Если каких-то данных нет — укажи это как отдельную проблему."""


async def analyze_card(
    product_brief: str,
    marketplace:   str,
    user_id:       int,
    username:      str | None,
) -> str:
    """
    Аудит карточки товара. Возвращает форматированный текст анализа.
    Использует ТОЛЬКО данные из product_brief.
    """
    messages = [
        {"role": "system", "content": _SYSTEM},
        {"role": "user",   "content": _prompt(product_brief, marketplace)},
    ]
    return await llm.call(messages, user_id=user_id, username=username, service="analysis")
