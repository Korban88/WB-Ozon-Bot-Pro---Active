"""
Модуль 1: Анализ карточки товара.

Принимает данные карточки и возвращает структурированный анализ:
оценку элементов, список проблем, приоритетные рекомендации.

Правило: анализ строится ТОЛЬКО на переданных данных.
Никаких выдуманных метрик конкурентов и средних по категории.
"""

from services import openrouter as llm

_SYSTEM = """Ты — эксперт по аналитике карточек маркетплейсов Wildberries и Ozon.
Твоя задача — давать конкретные, честные рекомендации для роста CTR и продаж.
Пиши структурированно, используй эмодзи для удобства чтения.
Не придумывай данные о конкурентах или рейтингах категорий — если данных нет, не упоминай это."""


def _prompt(brief: str, marketplace: str) -> str:
    mp = {"wb": "Wildberries", "ozon": "Ozon"}.get(marketplace, marketplace)
    return f"""Проанализируй карточку товара на {mp}.

ДАННЫЕ КАРТОЧКИ:
{brief}

Структура ответа:

📊 ОЦЕНКА (по 10 баллов)
— Название / заголовок: X/10
— Описание и ключевые слова: X/10
— УТП и позиционирование: X/10
— Итого: X/10

⚠️ ПРОБЛЕМЫ
(конкретно что мешает продажам, по пунктам)

✅ РЕКОМЕНДАЦИИ
(по приоритету: что сделать в первую очередь)

✍️ ЧТО ПЕРЕПИСАТЬ
(конкретные правки в тексте карточки)

Важно: основывайся только на предоставленных данных."""


async def analyze_card(
    product_brief: str,
    marketplace:   str,
    user_id:       int,
    username:      str | None,
) -> str:
    """
    Анализирует карточку товара. Возвращает форматированный текст анализа.
    Использует ТОЛЬКО данные из product_brief — не изобретает метрики.
    """
    messages = [
        {"role": "system", "content": _SYSTEM},
        {"role": "user",   "content": _prompt(product_brief, marketplace)},
    ]
    return await llm.call(messages, user_id=user_id, username=username, service="analysis")
